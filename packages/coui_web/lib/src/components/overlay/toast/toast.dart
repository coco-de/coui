
/// Position of the toast notification on screen.
enum ToastPosition {
  /// Bottom-center edge.
  bottomCenter,

  /// Bottom-right corner.
  bottomEnd,

  /// Bottom-left corner.
  bottomStart,

  /// Exact center of screen.
  middleCenter,

  /// Middle-right edge.
  middleEnd,

  /// Middle-left edge.
  middleStart,

  /// Top-center edge.
  topCenter,

  /// Top-right corner.
  topEnd,

  /// Top-left corner.
  topStart,
}
